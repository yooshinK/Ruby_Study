# Result = String / .class 가 데이터형식을 체크
name = 'yooshink'
puts('yooshink')
puts(name.class)

# Result = Array
print("Names - ")
names = ['yooshin','TEST',"Noah"]
puts(['yooshin','TEST',"Noah"].class)
puts(names.class)
puts(names)
puts(names[0])
puts(names[1])
puts(names[2])

print("A Person - ")
